_APPROX_OCTANT_GLYPHS = {
    90: "▞",
    165: "▚",
}


def _build_octant_tables() -> tuple[tuple[str, ...], tuple[bool, ...]]:
    glyphs: list[str] = [" "] * 256
    swap_flags: list[bool] = [False] * 256

    glyphs[0] = " "
    glyphs[255] = "█"

    for mask, codepoint in _OCTANT_CODEPOINTS.items():
        glyphs[mask] = chr(codepoint)

    for mask, glyph in _EXACT_OCTANT_GLYPHS.items():
        glyphs[mask] = glyph

    for mask, glyph in _APPROX_OCTANT_GLYPHS.items():
        glyphs[mask] = glyph

    for mask in range(1, 255):
        if glyphs[mask] != " ":
            continue
        complement = 0xFF ^ mask
        glyph = glyphs[complement]
        if glyph == " ":
            raise RuntimeError(f"Missing octant glyph fallback for mask {mask}")
        glyphs[mask] = glyph
        swap_flags[mask] = True

    return tuple(glyphs), tuple(swap_flags)


OCTANT_GLYPHS, OCTANT_GLYPH_SWAP = _build_octant_tables()
OCTANT_CANONICAL_MASKS = tuple(mask for mask in range(1, 256) if mask & 0x01)
